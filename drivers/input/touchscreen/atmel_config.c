static int atmel_platform_power(int on)
{
	int rc = -EINVAL;
#if defined (CONFIG_MACH_ATLAS32)||defined (CONFIG_MACH_BLADE2)||defined (CONFIG_MACH_ROAMER2)
	rc = gpio_request(107, "touch voltage");
	if (rc) {
		pr_err("%s: unable to request gpio 107 (%d)\n",
			__func__, rc);
	}
	if (on){
        	gpio_direction_output(107, 1);
		printk("%s: gpio %d = %d\n", __func__, 107, gpio_get_value(107));
	}
	else{
  		gpio_direction_output(107, 0);
	}
	//printk("xym---atmel_platform_power gpio107\n");//xym
  	gpio_free(107);
#elif defined (CONFIG_MACH_ATLAS40)
rc = gpio_request(13, "touch voltage");
if (rc) {
	pr_err("%s: unable to request gpio 13 (%d)\n",
		__func__, rc);
}
if (on){
	gpio_direction_output(13, 1);
		printk("%s: gpio %d = %d\n", __func__, 13, gpio_get_value(13));	
}
else{
	gpio_direction_output(13, 0);
}
//printk("xym---atmel_platform_power gpio107\n");//xym
gpio_free(13);

#else
	rc = gpio_request(107, "touch voltage");
	if (rc) {
		pr_err("%s: unable to request gpio 107 (%d)\n",
			__func__, rc);
	}
	if (on){
        	gpio_direction_output(107, 1);
		printk("%s: gpio %d = %d\n", __func__, 107, gpio_get_value(107));
	}
	else{
  		gpio_direction_output(107, 0);
	}
  	gpio_free(107);
#endif  	
	return 0;
}


